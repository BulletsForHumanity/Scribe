    // ── XML Documentation (BlockScope post-decoration) ───────────────────

    [Fact]
    public void Summary_InsertsXmlDocBeforeBlockHeader()
    {
        var q = new Quill();
        using (q.Block("public void Foo()").Summary("Does the foo thing."))
