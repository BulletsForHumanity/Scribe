    [Fact]
    public void Ref_UsingsAreSorted()
    {
        var q = new Quill();
        q.Ref("global::System.Threading.Tasks.Task");
        q.Ref("global::System.Collections.Generic.List");
        q.Ref("global::System.Guid");
        q.Line("global::System.Threading.Tasks.Task t = default;");
        q.Line("global::System.Collections.Generic.List<int> l = default;");
        q.Line("global::System.Guid g = default;");
        var result = q.Inscribe();

        var sysIdx = result.IndexOf("using System;", StringComparison.Ordinal);
        var genIdx = result.IndexOf("using System.Collections.Generic;", StringComparison.Ordinal);
