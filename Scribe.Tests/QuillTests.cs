    // ── Block ────────────────────────────────────────────────────────────

    [Fact]
    public void Block_EmitsHeaderBracesAndIndent()
    {
        var q = new Quill();
        using (q.Block("public class Foo"))
        {
            q.Line("public int X { get; }");
        }

        var result = q.Inscribe();
        result.ShouldContain("public class Foo");
        result.ShouldContain("{");
        result.ShouldContain("    public int X { get; }");
        result.ShouldContain("}");
    }

    [Fact]
    public void Block_NestedBlocks_IndentCorrectly()
    {
        var q = new Quill();
        using (q.Block("namespace Foo"))
        {
            using (q.Block("public class Bar"))
            {
                q.Line("public int X { get; }");
            }
        }

        var result = q.Inscribe();
        result.ShouldContain("    public class Bar");
        result.ShouldContain("        public int X { get; }");
    }

    [Fact]
    public void Block_MultiLineHeader_DedentsAndIndents()
    {
        var q = new Quill();
        using (
            q.Block(
                """
                public static bool TryParse(
                    string? s,
                    out Foo result)
                """
            )
        )
        {
            q.Line("result = default;");
        }

        var result = q.Inscribe();
        result.ShouldContain("public static bool TryParse(");
        result.ShouldContain("    string? s,");
        result.ShouldContain("    out Foo result)");
    }

    [Fact]
    public void Block_TrimsTrailingBlankLinesBeforeClose()
    {
        var q = new Quill();
        using (q.Block("public class Foo"))
        {
            q.Line("var x = 1;");
            q.Line(); // trailing blank
            q.Line(); // trailing blank
        }

        var result = q.Inscribe();
        // Should have exactly one newline between last content and }
        result.ShouldNotContain("var x = 1;\r\n\r\n\r\n}");
    }

