using Scribe;

namespace Scribe.Tests;

public sealed class QuillTests
{
    // ── Inscribe / Header ──────────────────────────────────────────────────

    [Fact]
    public void Inscribe_EmitsAutoGeneratedHeader()
    {
        var result = new Quill().Inscribe();

        result.ShouldStartWith("// <auto-generated/>");
        result.ShouldContain("~ The Scribe");
        result.ShouldContain("github.com/BulletsForHumanity/Scribe");
        result.ShouldContain("#nullable enable");
    }

    [Fact]
    public void Inscribe_CanOnlyBeCalledOnce()
    {
        var q = new Quill();
        q.Inscribe();

        Should.Throw<InvalidOperationException>(() => q.Inscribe());
    }

    // ── Namespace ────────────────────────────────────────────────────────

    [Fact]
    public void FileNamespace_EmitsFileScopedNamespace()
    {
        var q = new Quill();
        q.FileNamespace("MyApp.Generated");
        var result = q.Inscribe();

        result.ShouldContain("namespace MyApp.Generated;");
    }

    [Fact]
    public void FileNamespace_EmptyString_DoesNotEmitNamespace()
    {
        var q = new Quill();
        q.FileNamespace("");
        var result = q.Inscribe();

        result.ShouldNotContain("namespace");
    }

    // ── Usings ───────────────────────────────────────────────────────────

    [Fact]
    public void Using_EmitsSortedUsings()
    {
        var q = new Quill();
        q.Using("System.Linq");
        q.Using("System.Collections.Generic");
        var result = q.Inscribe();

        var linqIdx = result.IndexOf("using System.Collections.Generic;", StringComparison.Ordinal);
        var genIdx = result.IndexOf("using System.Linq;", StringComparison.Ordinal);
        linqIdx.ShouldBeLessThan(genIdx);
    }

    [Fact]
    public void Using_DeduplicatesDuplicates()
    {
        var q = new Quill();
        q.Using("System.Linq");
        q.Using("System.Linq");
        var result = q.Inscribe();

        var count = result.Split("using System.Linq;").Length - 1;
        count.ShouldBe(1);
    }

    [Fact]
    public void Usings_AddsMultiple()
    {
        var q = new Quill();
        q.Usings("System", "System.Linq");
        var result = q.Inscribe();

        result.ShouldContain("using System;");
        result.ShouldContain("using System.Linq;");
    }

    // ── Line ─────────────────────────────────────────────────────────────

    [Fact]
    public void Line_EmitsTextAtCurrentIndent()
    {
        var q = new Quill();
        q.Line("public class Foo { }");
        var result = q.Inscribe();

        result.ShouldContain("public class Foo { }");
    }

    [Fact]
    public void Line_EmptyString_EmitsBlankLine()
    {
        var q = new Quill();
        q.Line("first");
        q.Line();
        q.Line("second");
        var result = q.Inscribe();

        result.ShouldContain("first");
        result.ShouldContain("second");
    }

    // ── Lines (multi-line string) ────────────────────────────────────────

    [Fact]
    public void Lines_RawString_DedentsAndIndents()
    {
        var q = new Quill();
        using (q.Block("public class Foo"))
        {
            q.Lines(
                """
                var x = 1;
                var y = 2;
                """
            );
        }

        var result = q.Inscribe();
        result.ShouldContain("    var x = 1;");
        result.ShouldContain("    var y = 2;");
    }

    [Fact]
    public void Lines_Padded_InsertsBlankLinesAroundContent()
    {
        var q = new Quill();
        using (q.Block("public class Foo"))
        {
            q.Line("var a = 0;");
            q.Lines(
                    """
                    var x = 1;
                    var y = 2;
                    """
                )
                .Padded();
            q.Line("var z = 3;");
        }

        var result = q.Inscribe();
        // Verify blank line before the padded content
        result.ShouldContain("var a = 0;\r\n\r\n    var x = 1;");
        // Verify blank line after the padded content
        result.ShouldContain("var y = 2;\r\n\r\n    var z = 3;");
    }

    [Fact]
    public void Lines_ParamsArray_EmitsEachLine()
    {
        var q = new Quill();
        q.Lines("first", "second", "third");
        var result = q.Inscribe();

        result.ShouldContain("first");
        result.ShouldContain("second");
        result.ShouldContain("third");
    }

    // ── LinesFor ─────────────────────────────────────────────────────────

    [Fact]
    public void LinesFor_EmitsOneLinePerItem()
    {
        var q = new Quill();
        var items = new[] { "Alpha", "Beta", "Gamma" };
        q.LinesFor(items, x => $"// {x}");
        var result = q.Inscribe();

        result.ShouldContain("// Alpha");
        result.ShouldContain("// Beta");
        result.ShouldContain("// Gamma");
    }

    [Fact]
    public void LinesFor_Padded_WrapsInBlankLines()
    {
        var q = new Quill();
        q.Line("before");
        q.LinesFor(["item"], x => x).Padded();
        q.Line("after");
        var result = q.Inscribe();

        result.ShouldContain("before\r\n\r\nitem\r\n\r\nafter");
    }

