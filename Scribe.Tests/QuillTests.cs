    // ── Full integration: realistic output ───────────────────────────────

    [Fact]
    public void FullIntegration_ProducesCorrectOutput()
    {
        var q = new Quill();
        q.FileNamespace("TestDomain");
        var iParsable = q.Ref("global::System.IParsable");
        var argEx = q.Ref("global::System.ArgumentException");

        using (q.Block($"public partial record Amount : {iParsable}<Amount>"))
        {
            using (
                q.Block("public static Amount Create(decimal value)")
                    .Summary("Creates a new Amount.")
            )
            {
                q.Lines(
                    $$"""
                    if (value == default)
                        throw new {{argEx}}("bad", nameof(value));
                    return new Amount(value);
                    """
                );
            }

            q.Line("public static implicit operator decimal(Amount value) => value.Value;");
        }

        var result = q.Inscribe();

        result.ShouldContain("// <auto-generated/>");
        result.ShouldContain("using System;");
        result.ShouldContain("namespace TestDomain;");
        result.ShouldContain("/// <summary>Creates a new Amount.</summary>");
        result.ShouldContain("public partial record Amount : IParsable<Amount>");
        result.ShouldContain("        throw new ArgumentException(\"bad\", nameof(value));");
        result.ShouldContain(
            "    public static implicit operator decimal(Amount value) => value.Value;"
        );
        result.ShouldNotContain("global::");
    }

